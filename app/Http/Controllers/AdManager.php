     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        //
        if ($request->file('ad_photo')->isValid()) {
            $ad = new Ad;
            $ad->filename = $request->ad_photo->store('ads');
            $ad->save();

            return redirect(route('admin.admanager'))->with('success', 'File Saved');
        }
    }

